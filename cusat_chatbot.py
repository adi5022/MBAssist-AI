def detect_language(text: str) -> tuple[str, str]:
    """Returns (lang_code, lang_name)."""
    try:
        code = detect(text)
    except Exception:
        code = "en"
    name = LANGUAGE_NAMES.get(code, code.upper())
    return code, name

