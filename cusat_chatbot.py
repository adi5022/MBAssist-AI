def ask(question: str, verbose: bool = True) -> str:
    """Run the RAG pipeline and return the answer."""
    if verbose:
        print(f"\n{'─'*60}")
        print(f"Q: {question}")
    result = app.invoke({"query": question})
    answer = result["answer"]
    if verbose:
        print(f"A: {answer}")
        print(f"{'─'*60}")
    return answer


